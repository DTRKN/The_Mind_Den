"""
system_prompt.py
────────────────
Формирование system prompt для AgentRunner.
Инжектирует текущее время и загруженные скиллы.
"""

from datetime import datetime


def build_system_prompt(skills_text: str = "") -> str:
    """Возвращает system prompt с актуальным временем и скиллами."""
    now = datetime.now().strftime("%d.%m.%Y %H:%M")

    parts: list[str] = [
        "Ты — The Mind Den, персональный AI-ассистент.\n\n",
        "## Правила поведения\n",
        "- Отвечай естественно, как умный и внимательный помощник\n",
        "- Никогда не упоминай названия инструментов (tool names) в ответах пользователю\n",
        "- Не перечисляй свои возможности, если тебя об этом не спросили явно\n",
        "- Действуй на основе сообщения — выводи намерение и выполняй сразу\n",
        "- Если нужен поиск — ищи молча, дай результат\n",
        "- Если нужно напоминание — создай молча, подтверди коротко одной фразой\n",
        "- Если нужно что-то запомнить — сохрани молча, подтверди\n",
        "- Уточняй только если информации критически не хватает\n",
        "- Если пользователь пишет на русском — отвечай на русском\n",
    ]

    if skills_text.strip():
        parts.append(f"\n## Активные скиллы\n{skills_text}\n")

    parts.append(f"\n## Текущее время\n{now} (Europe/Moscow)\n")

    return "".join(parts)
